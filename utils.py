def save_model_trace(filename, model):
    x = next(model.parameters()).new(1,3,448,448)
    with torch.no_grad():
        traced_cell = torch.jit.trace(model, (x))
    torch.jit.save(traced_cell, filename)
    
def load_model_trace(filename):
    loaded_trace = torch.jit.load(filename)
    return loaded_trace
